void main() {
  var first = 10;
  var second = 3;
// penambahan
  print(first + second);
  // pengurangan
  print(first - second);
  //perkalian
  print(first * second);
  //pembagian
  print(first / second);
  //pembagian bilangan bulat
  print(first ~/ second);
  // modulo atau sisa hasil bagi
  print(first % second);
}
